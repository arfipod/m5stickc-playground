static uint32_t ledPulseUntilMs = 0;

void onMenuItemSelected(const MenuItem& item) {
  Serial.printf("Selected menu item %u: %s\n", item.id, item.title);
  M5.Beep.tone(2400 + item.id * 180, 80);
  digitalWrite(10, LOW);
  ledPulseUntilMs = millis() + 90;
}

static const MenuItem kMenuItems[] = {
    {0, "Music", kIconMusic, 32, 32, kMusicColor, onMenuItemSelected},
    {1, "Timer", kIconTimer, 32, 32, kTimerColor, onMenuItemSelected},
    {2, "Settings", kIconSettings, 32, 32, kSettingsColor,
     onMenuItemSelected},
    {3, "WiFi", kIconWifi, 32, 32, kWifiColor, onMenuItemSelected},
    {4, "Info", kIconInfo, 32, 32, kInfoColor, onMenuItemSelected},
};

static CoverFlowMenu menu(kMenuItems,
                          sizeof(kMenuItems) / sizeof(kMenuItems[0]));

static bool imuReady = false;
static uint32_t lastTiltNavMs = 0;
static uint32_t lastUpdateMs = 0;
static uint32_t lastDrawMs = 0;
static bool buttonALongConsumed = false;

CoverFlowInputState readInput(uint32_t now) {
  CoverFlowInputState input = {false, false, false, 0};

  if (M5.BtnA.pressedFor(650) && !buttonALongConsumed) {
    input.selectPressed = true;
    buttonALongConsumed = true;
  }

  if (M5.BtnA.wasReleased()) {
    if (!buttonALongConsumed) {
      input.nextPressed = true;
    }
    buttonALongConsumed = false;
  }

  if (M5.BtnB.wasReleased()) {
    input.previousPressed = true;
  }

  if (!input.nextPressed && !input.previousPressed && !input.selectPressed &&
      imuReady && now - lastTiltNavMs >= 320) {
    float accX = 0.0f;
    float accY = 0.0f;
    float accZ = 0.0f;
    M5.IMU.getAccelData(&accX, &accY, &accZ);

    if (accY > 0.58f) {
      input.tiltDirection = 1;
      lastTiltNavMs = now;
    } else if (accY < -0.58f) {
      input.tiltDirection = -1;
      lastTiltNavMs = now;
    }
  }

  return input;
}

void setup() {
  M5.begin();
  Serial.begin(115200);

  pinMode(10, OUTPUT);
  digitalWrite(10, HIGH);

  M5.Lcd.setRotation(3);
  M5.Lcd.setTextWrap(false);
  M5.Lcd.fillScreen(BLACK);

  M5.Beep.setVolume(4);
  imuReady = M5.Imu.Init() == 0;

  bool buffered = menu.begin(M5.Lcd, M5.Lcd.width(), M5.Lcd.height());
  menu.draw(M5.Lcd);

  lastUpdateMs = millis();
  lastDrawMs = lastUpdateMs;

  Serial.printf("Cover Flow demo ready. Sprite buffer: %s. IMU: %s.\n",
                buffered ? "yes" : "no", imuReady ? "yes" : "no");
}

void loop() {
  M5.update();
  M5.Beep.update();

  uint32_t now = millis();
  uint32_t deltaMs = now - lastUpdateMs;
  lastUpdateMs = now;

  CoverFlowInputState input = readInput(now);
  menu.update(input, deltaMs);

  if (now - lastDrawMs >= 33) {
    menu.draw(M5.Lcd);
    lastDrawMs = now;
  }

  if (ledPulseUntilMs != 0 && now >= ledPulseUntilMs) {
    digitalWrite(10, HIGH);
    ledPulseUntilMs = 0;
  }

  delay(1);
}
